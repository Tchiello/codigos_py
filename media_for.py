# fazer média de cada aluno

for i in range(0, 2):
    nota1 = float(input('digita e primeira nota:'))
    nota2 = float(input('digita e segunda nota:'))

    media = (nota1 + nota2) / 2
    print (f'Aluno {i+1} - Média: {media}')

                    
